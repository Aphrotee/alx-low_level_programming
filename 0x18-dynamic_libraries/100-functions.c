#include "main.h"

/**
 * mul - returns the product of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: product of a and b
 */
int mul(int a, int b)
{
	int d = a * b;

	return (d);
}

/**
 * add - returns the sum of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of a and b
 */
int add(int a, int b)
{
	int d = a + b;

	return (d);
}

/**
 * div - returns the division of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: division of a by b
 */
int div(int a, int b)
{
	int d = a / b;

	return (d);
}

/**
 * sub - returns the subtraction of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: subtraction of b from a
 */
int sub(int a, int b)
{
	int d = a - b;

	return (d);
}

/**
 * mod - returns the modular division of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: modular division of a and b i.e
 * divides a by b and returns the remainder
 */
int mod(int a, int b)
{
	int d = a % b;

	return (d);
}
